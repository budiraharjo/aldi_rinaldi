“Kabupaten Pandeglang Tanggap, Tangkas dan Tangguh Dalam Penanggulangan Bencana”
</p>
<p style=" text-align: justify; text-justify: inter-word;"><b>
Misi</b>
</p>
<p style=" text-align: justify; text-justify: inter-word;"> 
Misi merupakan hal-hal yang harus diemban atau dilaksanakan oleh BPBD Kabupaten Pandeglang untuk menjabarkan dan mencapai Visi yang telah ditetapkan.
Berdasarkan  uraian Visi di atas, maka Misi BPBD Kabupaten Pandeglang adalah:</p></br>
<p>1.	Pengembangan kearifan lokal masyarakat di bidang kebencanaan;</p>
<p>2.	Terwujudnya sistem penanggulangan bencana yang efektif, efisien, handal dan terkoordinasi;</p>
<p>3.	Melakukan pemulihan melalui usaha rehabilitasi dan rekonstruksi pasca bencana.</p>
</br>
<p>Ketiga misi tersebut dapat dijelaskan sebagai berikut:
<p>1)	Meningkatkan pengembangan kearifan lokal masyarakat di bidang kebencanaan, ditujukan untuk mendukung budaya dan kebiasaan masyarakat setempat dalam penanggulangan bencana di daerahnya.</p>
<p>2)	Memberdayakan seluruh elemen masyarakat yang ada dalam menghadapi bencana yang terjadi dalam rangka pengurangan risiko bencana.</p>
<p>3)	Meningkatkan layanan kebencanaan masyarakat, ditujukan untuk meningkatkan pelayananan publik dalam memperoleh informasi dan bantuan kebencanaan masyarakat.</p>
<p>4)	Meningkatkan volume bantuan rehabilitasi dan rekonstruksi pasca bencana di bidang infrastruktur khususnya di lokasi terdampak bencana, ditujukan untuk menyediakan dukungan bagi peningkatan pelayanan dasar.</p>
<p>5)	Meningkatkan kualitas SDM yang agamis, cerdas, kreatif dan inovatif, ditujukan untuk menciptakan pelaku penanggulangan bencana yang cerdas, terampil dan berakhlak mulia dalam melaksanakan penanganan bencana di Kabupaten Pandeglang.</p>
<p>6)	Meningkatkan tata kelola kebencanaan, ditujukan untuk menciptakan pengelola bidang kebencanaan yang baik dan bersih dalam melakukan pelayanan publik di seluruh sektor dan wilayah bencana.</p>